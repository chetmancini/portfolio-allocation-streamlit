
def format_as_pct(value: float) -> str:
    return f"{value:.2f}%"

def float_pct(value: float) -> float:
    """round float to two decimal points"""
    return round(value, 2)

def float_dollars(value: float) -> float:
    """round float to two decimal points"""
    return round(value, 2)

def format_dollars(value: float) -> str:
    return f"${value:.2f}"

